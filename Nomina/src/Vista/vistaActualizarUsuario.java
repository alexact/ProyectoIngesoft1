    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        ArrayList rolesEscogidos = new ArrayList(4);
        UsuarioControl controll = new UsuarioControl(0,"","",rolesEscogidos,ConsultarCedula.getText());
        try {
            String nombreEncontrado = controll.devolverUsuario().validarCedula();
            if(nombreEncontrado.equals("")){
                vistaUsuarioNoEncontrado error = new vistaUsuarioNoEncontrado();
                error.setVisible(true);
            }else{
                usuarioACambiar.setText(nombreEncontrado);
            }   
        } catch (SQLException ex) {
            Logger.getLogger(vistaActualizarUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

